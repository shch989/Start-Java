package ch04;

public class BinaryTest {

	public static void main(String[] args) {
		// 10진수
		int num = 10;
		// 2진수
		int bNum = 0B1010;
		// 8진수
		int gNum = 012;
		// 16진수
		int xNum = 0XA;
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(gNum);
		System.out.println(xNum);
	}

}
