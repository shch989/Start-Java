package ch06;

public class VariableTest {

	public static void main(String[] args) {
		
		// 1bit
		byte bnum = -127;
		// 2bit
		short snum = -12345;
		// 4bit
		int num = 1234567890;
		// 8bit
		long lnum = 12345678900L;
		
		System.out.println(bnum);
		System.out.println(snum);
		System.out.println(num);
		System.out.println(lnum);
	}

}
